        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");//load drive
            String url = "jdbc:sqlserver://localhost:1433;databaseName=QLSV31";
            String user = "sa";
            String password = "12345";
            Connection con = DriverManager.getConnection(url, user, password);
            String sql = "SELECT * FROM USERS WHERE username = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, txt1.getText());
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                String role = rs.getString(3);
                if (role.equals("DT")) {
                    JOptionPane.showMessageDialog(rootPane, "Đăng nhập thành công");
                    new QLSV(role).setVisible(true);
                      this.dispose();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Đăng nhập thành công");
                    new QLDSV(role).setVisible(true);
                       this.dispose();
                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }

